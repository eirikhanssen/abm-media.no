    <h3>Sammendrag</h3>
    <p>Conference theme: Design Learning for Tomorrow - Design Education from Kindergarten to PhD. Designed artefacts and solutions influence our lives and values, both from a personal and societal perspective. Designers, decision makers, investors and consumers hold different positions in the design process, but they all make choices that will influence our future visual and material culture. To promote sustainability and meet global challenges for the future, professional designers are dependent on critical consumers and a design literate general public. For this purpose design education is important for all. We propose that design education in general education represents both a foundation for professional design education and a vital requirement for developing the general public&#x2019;s competence for informed decision making.</p>
