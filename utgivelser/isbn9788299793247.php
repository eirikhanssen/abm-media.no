    <h3>Sammendrag</h3>
    <p>Formålet med boka er å stimulera til debatt om dei teoretiske og metodologiske føresetnadene for arkivarbeid i vid forstand. Forfattaren er sterkt påverka av den samfunnsorienterte og maktkritiske arkivfaglege diskursen som oppstod på 1990-talet. Boka har kapittel om arkivets makt, frå fag til vitskap, arkiv og samfunnsminne, arkivdanning og arkivsystem, arkivbeskrivning, arkiva til alle, demokratisering av arkivet.</p>
    <h3>Om forfatteren</h3>
    <h4>Gudmund Valderhaug</h4>
    <p>Gudmund Valderhaug har undervist i arkiv ved Institutt for arkiv, bibliotek og informasjonsfag ved Høgskolen i oslo og Akershus sidan 2004. Han er cand. Philol. Med historie hovudfag frå Universitetet i bergen.</p>
    <p>Han har arbeidd som arkivar ved Bergen Byarkiv (1983-86), interkommunalt arkiv i Hardoland (1987-2003) og Hordaland Fylkesarkiv (2006-10) og var leiar for Landslaget for lokal og privatarkiv frå 1997 til 2003. Frå 2003-2006 var han avdelingsdirektør i ABM-utvikling med ansvar for arkivfeltet. Han er redaktør av TidsskriftetArkiv og står bak bloggen depotdrengen.wordpress.com</p>
