    <h3>Sammendrag</h3>
    <p>Form&aring;let med boka er &aring; stimulera til debatt om dei teoretiske og metodologiske f&oslash;resetnadene for arkivarbeid i vid forstand. Forfattaren er sterkt p&aring;verka av den samfunnsorienterte og maktkritiske arkivfaglege diskursen som oppstod p&aring; 1990-talet. Boka har kapittel om arkivets makt, fr&aring; fag til vitskap, arkiv og samfunnsminne, arkivdanning og arkivsystem, arkivbeskrivning, arkiva til alle, demokratisering av arkivet.</p>
    <h3>Om forfatteren</h3>
    <h4>Gudmund Valderhaug</h4>
    <p>Gudmund Valderhaug har undervist i arkiv ved Institutt for arkiv, bibliotek og informasjonsfag ved H&oslash;gskolen i oslo og Akershus sidan 2004. Han er cand. Philol. Med historie hovudfag fr&aring; Universitetet i bergen.</p>
    <p>Han har arbeidd som arkivar ved Bergen Byarkiv (1983-86), interkommunalt arkiv i Hardoland (1987-2003) og Hordaland Fylkesarkiv (2006-10) og var leiar for Landslaget for lokal og privatarkiv fr&aring; 1997 til 2003. Fr&aring; 2003-2006 var han avdelingsdirekt&oslash;r i ABM-utvikling med ansvar for arkivfeltet. Han er redakt&oslash;r av TidsskriftetArkiv og st&aring;r bak bloggen depotdrengen.wordpress.com</p>
