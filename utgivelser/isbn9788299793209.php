    <h3>Sammendrag</h3>
    <p>Alle de nordiske landene har i dag lover om bibliotektjeneste og bibliotekorganisering. Folkebibliotekene st&aring;r sentralt i disse lovene, men i ulikt omfang har biblioteklovene ogs&aring; bestemmelser som regulerer virksomheten i andre bibliotek. Boka gj&oslash;r rede for kva som er lovfestet i det enkelte land og sammenlikner innholdet i lovbestemmelsene og graden av lovregulering.
    </p>
    <h3>Om forfatteren</h3>
    <h4>Asbj&oslash;rn Langeland</h4>
    <p>Asbj&oslash;rn Langeland var direkt&oslash;r for Statens Bibliotektilsyn fr&aring; 1991-2002. Deretter jobbet han som direkt&oslash;r for NORDBOK, den nordiske litteratur og bibliotek-komiteen. Han har ogs&aring; erfaring som biblioteksjef i Skedsmo/fylkesbiblioteksjef i Akershus 1966&ndash;79, og f&oslash;rstekonsulent i&nbsp;Statens bibliotektilsyn 1979&ndash;83, privat konsulentvirksomhet 1983-88, avdelingsleder&nbsp;Statens bibliotek- og informasjonsh&oslash;gskole 1988&ndash;91.</p>
