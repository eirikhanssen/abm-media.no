    <h3>Sammendrag</h3>
    <p>Alle de nordiske landene har i dag lover om bibliotektjeneste og bibliotekorganisering. Folkebibliotekene står sentralt i disse lovene, men i ulikt omfang har biblioteklovene også bestemmelser som regulerer virksomheten i andre bibliotek. Boka gjør rede for kva som er lovfestet i det enkelte land og sammenlikner innholdet i lovbestemmelsene og graden av lovregulering.
    </p>
    <h3>Om forfatteren</h3>
    <h4>Asbjørn Langeland</h4>
    <p>Asbjørn Langeland var direktør for Statens Bibliotektilsyn frå 1991-2002. Deretter jobbet han som direktør for NORDBOK, den nordiske litteratur og bibliotek-komiteen. Han har også erfaring som biblioteksjef i Skedsmo/fylkesbiblioteksjef i Akershus 1966–79, og førstekonsulent i&nbsp;Statens bibliotektilsyn 1979–83, privat konsulentvirksomhet 1983-88, avdelingsleder&nbsp;Statens bibliotek- og informasjonshøgskole 1988–91.</p>
