    <h3>Sammendrag</h3>
    <p>&#x2018;Kunnskapstriangelet&#x2019; – samspel mellom utdanning, forsking og innovasjon- er mål både for utdannings- og forskingskvalitet og for demokratiske samfunnsprosessar. Med utgangspunkt i &#x2018;kunnskapstriangelet&#x2019; er målet med denne boka å trekkje fram yrkesretta profesjonsutdanningar sine kunnskapsoppdrag – å kvalifisere personell til yrkeskvalitet i ein utviklingsorientert praksis som er vitskapleg fundert. Det er i utveksling mellom teoretisk kunnskap og praktisk utøving slik kvalitet blir utvikla og særtrekk ved fagleg utviklingsarbeid blir interessante.</p>
    <p>Varierte eksempel frå utviklingsarbeid i ulike utdannings- og yrkesområde er stamma i boka. Omkring den er det samla nokre meir generelle bidrag: Om rammer, metodar og teoretiske tilnærmingar med tanke på å utvikle ny praksis og ny kunnskap.</p>
    <h3>Om redaktørene</h3>
    <h4>Gerd Bjørke</h4>
    <p>Gerd Bjørke var tidlegare dosent i høgskolepedagogikk ved Høgskolen i Oslo og Akershus og er i dag tilknytt Høgskolen Stord/Haugesund (hovudredaktør).</p>
    <h4>Harald Jarning</h4>
    <p>Harald Jarning er dosent i pedagogikk ved grunnskolelærerutdanninga ved Høgskolen i Oslo og Akershus</p>
    <h4>Olav Eikeland</h4>
    <p>Olav Eikeland er dr. philos. og prodekan for FoU ved Fakultet for lærerutdanning og internasjonale studier, Høgskolen i Oslo og Akershus.</p>
