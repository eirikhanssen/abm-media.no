    <h3>Sammendrag</h3>
    <p>&#x2018;Kunnskapstriangelet&#x2019; &ndash; samspel mellom utdanning, forsking og innovasjon- er m&aring;l b&aring;de for utdannings- og forskingskvalitet og for demokratiske samfunnsprosessar. Med utgangspunkt i &#x2018;kunnskapstriangelet&#x2019; er m&aring;let med denne boka &aring; trekkje fram yrkesretta profesjonsutdanningar sine kunnskapsoppdrag &ndash; &aring; kvalifisere personell til yrkeskvalitet i ein utviklingsorientert praksis som er vitskapleg fundert. Det er i utveksling mellom teoretisk kunnskap og praktisk ut&oslash;ving slik kvalitet blir utvikla og s&aelig;rtrekk ved fagleg utviklingsarbeid blir interessante.</p>
    <p>Varierte eksempel fr&aring; utviklingsarbeid i ulike utdannings- og yrkesomr&aring;de er stamma i boka. Omkring den er det samla nokre meir generelle bidrag: Om rammer, metodar og teoretiske tiln&aelig;rmingar med tanke p&aring; &aring; utvikle ny praksis og ny kunnskap.</p>
