    <h3>Sammendrag</h3>
    <p>Bibliotekarene skal gi tilgang til informasjon, kunnskap og kulturelle og litter&aelig;re opplevelser. Hva slags kunnskap og kompetanse trenger bibliotekarer i det 21.&aring;rhundre? Hvilke krav stilles til dem som skal utdanne fagpersoner til denne profesjonen?</p>
    <p>I 2010 var det 70 &aring;r siden bibliotek- og informasjonsfaglig utdanning ble etablert i Norge. Utdanningen har utviklet seg fra en yrkesoppl&aelig;ring til en forskningsforankret profesjonsutdanning. Faglig ansatte ved Institutt for arkiv-, bibliotek- og informasjonsfag ved Fakultet for samfunnsfag, H&oslash;gskolen i Oslo og Akershus, har brukt det som et utgangspunkt for forskningsbasert refleksjon om fagets grunnlag - dets framtid og historie.</p>
    <h3>Om redakt&oslash;ren</h3>
    <h4>Ragnar Audunsson</h4>Ragnar Audunsson som har redigert boka er professor ved bibliotekutdanningen ved H&oslash;gskolen i oslo og Akershus.
