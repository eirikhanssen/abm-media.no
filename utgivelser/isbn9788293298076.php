    <h3>Sammendrag</h3>
    <p>I heftet går Åsulv Frøysnes summarisk gjennom høgskolenes historie fra 1960-årene med blant annet Ottosen-komiteen som lanserte distriktshøgskolene og laget et lite studentopprør i Norge. Han ser på etableringen av de regionale høgskolestyrene og Hernes sitt reformarbeid med høgskolereformen i 1994 med et spesielt på situasjonen i Oslo og Akershus.</p>
    <h3>Om forfatteren</h3>
    <h4>Åsulv Frøysnes</h4>
    <p>Åsulv Frøysnes var høgskoledirektør ved Høgskolen i Oslo i 17 år fram til 2011 da han gikk av med pensjon. Før det jobbet han i over 20 år som både saksbehandler, byråsjef for lærerutdanning og underdirektør i Høgskoleseksjonen i det som den gangen het Kyrkje- og&nbsp;undervisningsdepartementet.</p>
