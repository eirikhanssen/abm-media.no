    <p>&Aring;sulv Fr&oslash;ysnes var h&oslash;gskoledirekt&oslash;r ved H&oslash;gskolen i Oslo i 17 &aring;r fram til 2011 da han gikk av med pensjon. F&oslash;r det jobbet han i over 20 &aring;r som b&aring;de saksbehandler, byr&aring;sjef for l&aelig;rerutdanning og underdirekt&oslash;r i H&oslash;gskoleseksjonen i det som den gangen het Kyrkje- og&nbsp;undervisningsdepartementet.</p>
