    <h3>Sammendrag</h3>
    <p>I heftet g&aring;r &Aring;sulv Fr&oslash;ysnes summarisk gjennom h&oslash;gskolenes historie fra 1960-&aring;rene med blant annet Ottosen-komiteen som lanserte distriktsh&oslash;gskolene og laget et lite studentoppr&oslash;r i Norge. Han ser p&aring; etableringen av de regionale h&oslash;gskolestyrene og Hernes sitt reformarbeid med h&oslash;gskolereformen i 1994 med et spesielt p&aring; situasjonen i Oslo og Akershus.</p>
    <h3>Om forfatteren</h3>
