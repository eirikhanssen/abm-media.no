    <h3>Sammendrag</h3>
    <p>Formidleren av arkivmateriale stilles i dag overfor stadig nye utfordringer. Digitaliseringen av hverdagen og de sosiale medier innebærer et en må se utover hva som finnes i arkivet, hvordan finne fram til og forstå både arkivet og dets innhold. Arkiv inngår i en større kontekst. Antologien samler bidrag fra arkivfeltet i Norge, Sverige og Danmark. Boken retter seg mot forskere som er interessert i arkiv og formidling generelt, og mer spesifikt mot arkivarer og arkivformidlere samt studenter innen arkivvitenskap.</p>
    <h3>Om redaktørene</h3>
    <h4>Hilde Lange</h4>
    <p>Hilde Lange er arkivleder i Troms fylkeskommune.</p>
    <h4>Ulrike Spring</h4>
    <p>Ulrike Spring er førsteamanuensis ved Høgskulen i Sogn og Fjordane.</p>
