    <h3>Sammendrag</h3>
    <p>Formidleren av arkivmateriale stilles i dag overfor stadig nye utfordringer. Digitaliseringen av hverdagen og de sosiale medier inneb&aelig;rer et en m&aring; se utover hva som finnes i arkivet, hvordan finne fram til og forst&aring; b&aring;de arkivet og dets innhold. Arkiv inng&aring;r i en st&oslash;rre kontekst. Antologien samler bidrag fra arkivfeltet i Norge, Sverige og Danmark. Boken retter seg mot forskere som er interessert i arkiv og formidling generelt, og mer spesifikt mot arkivarer og arkivformidlere samt studenter innen arkivvitenskap.</p>
    <h3>Om redakt&oslash;rene</h3>
