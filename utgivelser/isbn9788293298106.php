    <h3>Sammendrag</h3>
    <p>Denne framstillinga er en oppfølging av artikkel om Høgre utdanning 1970-1994 med vekt på høgskolevirksomheten i Oslo og Akershus.  Framstillinga er først og fremst et grunnlagsmateriale med kilder for de som skal gjøre videre arbeid med problemstillinger knyttet til høgre utdanning. Den vil da være et bidrag i arbeidet med historien om Høgskolen i Oslo og Akershus som er under arbeid.</p>
	<p>Dokumentet gir en oversikt over noen viktige hendelser innen nasjonal politikk for høgre utdanning i åra 1994-2011. Den omhandler ellers noen sentrale spørsmål knyttet til høgre utdanning og forfatters erfaringer fra arbeid med høgre utdanningsspørsmål i departementet og ved Høgskolen i Oslo. Framstillinga er sett fra en administrativ synsvinkel og vil for en del bære preg av forfatterens personlige vurderinger og opplevelser. Formålet med artikkelen er at den skal inspirere til å gå nærmere inn i de saker og problemstillinger som er tatt opp.</p>
    
	<h3>Om forfatteren</h3>
    
	<h4>Åsulv Frøysnes</h4>
    <p>Åsulv Frøysnes var høgskoledirektør ved Høgskolen i Oslo i 17 år fram til 2011 da han gikk av med pensjon. Før det jobbet han i over 20 år som både saksbehandler, byråsjef for lærerutdanning og underdirektør i Høgskoleseksjonen i det som den gangen het Kyrkje- og undervisningsdepartementet.</p>

