    <h3>Sammendrag</h3>
    <p>Boken gir retningslinjer for innholdsanalyse og indeksering av dokumenter, og for oppbygging og vedlikehold av emneordskatalogen. Retningslinjene bygger på grunnleggende klassifikasjons- og indekseringsteori, og sees i sammenheng med internasjonale standarder og retningslinjer innen området bibliografisk kontroll.</p>
    <p>I denne tredje utgaven er sluttbrukerperspektivet blitt tydeligere. Det er lagt vekt på å forankre retningslinjene &nbsp;i katalogens mål og oppgaver, slik de er formulert i IFLAs "Statement of international cataloguing principles". &nbsp;FRBR-modellens funksjonskrav til autoritetsdata for emner (FRSAD) er også omtalt. &nbsp;Mange kommentaravsnitt er utvidet, enkelte eksempler er endret og nye tilføyd, og flere ordforklaringer er lagt til.</p>
    <h3>Om forfatteren</h3>
    <h4>Ellen Hjortsæter</h4>
    <p>Ellen Hjortsæter er pensjonist, men har vært førstelektor ved Høgskolen i Oslo og Akershus, Inst. for arkiv-, bibliotek- og informasjonsfag. Hennes spesialfelt er kunnskapsorganisasjon og gjenfinning. Hun var også ansatt i fleire år i Statens Bibliotektilsyn.</p>
