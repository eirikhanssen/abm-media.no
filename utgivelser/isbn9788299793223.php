    <h3>Sammendrag</h3>
    <p>Boken gir retningslinjer for innholdsanalyse og indeksering av dokumenter, og for oppbygging og vedlikehold av emneordskatalogen. Retningslinjene bygger p&aring; grunnleggende klassifikasjons- og indekseringsteori, og sees i sammenheng med internasjonale standarder og retningslinjer innen omr&aring;det bibliografisk kontroll.</p>
    <p>I denne tredje utgaven er sluttbrukerperspektivet blitt tydeligere. Det er lagt vekt p&aring; &aring; forankre retningslinjene &nbsp;i katalogens m&aring;l og oppgaver, slik de er formulert i IFLAs "Statement of international cataloguing principles". &nbsp;FRBR-modellens funksjonskrav til autoritetsdata for emner (FRSAD) er ogs&aring; omtalt. &nbsp;Mange kommentaravsnitt er utvidet, enkelte eksempler er endret og nye tilf&oslash;yd, og flere ordforklaringer er lagt til.</p>
    <h3>Om forfatteren</h3>
    <h4>Ellen Hjorts&aelig;ter</h4>
    <p>Ellen Hjorts&aelig;ter er pensjonist, men har v&aelig;rt f&oslash;rstelektor ved H&oslash;gskolen i Oslo og Akershus, Inst. for arkiv-, bibliotek- og informasjonsfag. Hennes spesialfelt er kunnskapsorganisasjon og gjenfinning. Hun var ogs&aring; ansatt i fleire &aring;r i Statens Bibliotektilsyn.</p>
