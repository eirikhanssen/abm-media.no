    <h3>Sammendrag</h3>
    <p>Med en fot i fortiden og en fot i fremtiden har byarkivar Arne Skivenes bygget opp Bergen Byarkiv til å bli en av landets fremste arkivinstitusjoner, - en institusjon som balanserer godt mellom det gamle og det nye. Den 20. juli 2017 fyller Arne 70 år og i den anledning har vi gleden av å gi ut festskriftet <em>«Han e’ søkkane go’!» Et festskrift til byarkivar Arne Skivenes.</em></p>
    <p>Som nyutdannet historiker i 1976 troppet Arne opp på Bergen kommunes informasjonskontor for å høre hvordan det gikk med planene for et kommunearkiv. Dette resulterte i et midlertidig engasjement for å ordne kommunale arkiver. I 1979 ble Bergen Byarkiv opprettet og Arne ble ansatt som Bergens første byarkivar.</p>
    <p>Arne Skivenes’ engasjement og faglige bidrag favner om de fleste feltene innen arkivfaget. Derfor er vi stolte over å kunne presentere et festskrift som inneholder 30 artikler med arkiv- og historiefaglig bredde forfattet av kloke hoder innen arkiv, bibliotek, museum, universitet og høyskoler.</p>
    <p>Publikasjonen er utgitt med støtte fra Byrådsavdeling for klima, kultur og næring/Bergen kommune, Kultur- og idrettsavdelinga/Hordaland fylkeskommune, Arkiv­forbundet, Arkivar­foreningen, Norsk Arkivråd, Forskerforbundet v/Bergen kommune, Stiftelsen Lokalhistorisk Arkiv i Bergen og Bergen Byarkiv.</p>
