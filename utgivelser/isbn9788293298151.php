    <h4>Gudmund Valderhaug</h4>
    <p>Gudmund har undervist i arkiv ved Institutt for arkiv, bibliotek og informasjonsfag ved Høgskolen i Oslo og Akershus sidan 2004. Han er cand. Philol. Med historie hovudfag frå Universitetet i bergen.</p>
    <p>Han har jobbet som arkivar ved Bergen Byarkiv (1983-86), interkommunalt arkiv i Hardoland (1987-2003) og Hordaland Fylkesarkiv (2006-10) og var leder for Landslaget for lokal og privatarkiv fra 1997 til 2003. Fra 2003-2006 var han avdelingsdirektør i ABM-utvikling med ansvar for arkivfeltet. Han er redaktør av TidsskriftetArkiv og står bak bloggen depotdrengen.wordpress.com</p>
