    <h3>Sammendrag</h3>
    <h4>Finner vi ikke ‘alt’ på nettet da?</h4>
    <p>Jon Bing svarte en gang på dette spørsmålet med å si at når ‘alt’ ble tilgjengelig på nett, ville det likevel være bruk for bibliotek og bibliotekarer. Bing mente at problemstillingen var «i slekt med spørsmålet om hvorfor man trenger lærere når man jo kan lese bøkene på egen hånd». </p>
    <p>Når det gjelder offentlig informasjon gir nettet god dekning av:</p>
    <ul>
    <li>Utredninger og rapporter</li>
    <li>Saker fra Storting og regjering</li>
    <li>Lov og rett</li>
    <li>Statistikk</li>
    <li>EU/EØS-publikasjoner</li>
    <li>Kommunale tjenester og politikk</li>

    </ul>
    <p>Det finnes mange gode nettsteder for offentlig informasjon, men det må likevel innrømmes at det er lett å rote seg bort.</p>
    <p>Dette er en veiviser for bibliotekarer – og andre som vil vite mer om hvordan man finner veien til de gode kildene.</p>
    <h3>Om forfatteren</h3>
    <p><strong>Øivind Frisvold</strong> har undervist i emnet offentlig informasjon siden 1980. Han har også holdt en rekke kurs og foredrag og skrevet artikler om emnet. Frisvold er førsteamanuensis ved Institutt for arkiv-, bibliotek- og informasjonsfag ved Høgskolen i Oslo og Akershus.</p>
