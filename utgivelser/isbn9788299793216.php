    <h3>Sammendrag</h3>
    <p>Festskriftet ble laget i forbinndelse med at Egil Nys&aelig;ter fratr&aring;dde som fylkesarkivar i Hordaland. Festskriftet speiler Nys&aelig;ters arbeid innenfor arkiv og lokalhistorie. Her er det artikler om utviklinga i lokalhistorisk forsking, om fotografi som kjelde, om etableringen av interkommunalt arkiv p&aring; Nord-Vestlandet, om arkiv og etikk, om formidling av arkivinformasjon, om bevaring av elektroniske arkiv og om statlig arkivpolitikk for &aring; nemne noen av temaene.</p>
    <h3>Om redakt&oslash;rene</h3>
    <h4>Anne Aune</h4>
    <h4>Valderhaug Gudmund</h4>
    <p>Gudmund Valderhaug har undervist i arkiv ved Institutt for arkiv, bibliotek og informasjonsfag ved H&oslash;gskolen i oslo og Akershus sidan 2004. Han er cand. Philol. Med historie hovudfag fr&aring; Universitetet i bergen.</p>
    <p>Han har arbeidd som arkivar ved Bergen Byarkiv (1983-86), interkommunalt arkiv i Hardoland (1987-2003) og Hordaland Fylkesarkiv (2006-10) og var leiar for Landslaget for lokal og privatarkiv fr&aring; 1997 til 2003. Fr&aring; 2003-2006 var han avdelingsdirekt&oslash;r i ABM-utvikling med ansvar for arkivfeltet. Han er redakt&oslash;r av TidsskriftetArkiv og st&aring;r bak bloggen <a href="http://depotdrengen.wordpress.com">depotdrengen.wordpress.com</a></p>
