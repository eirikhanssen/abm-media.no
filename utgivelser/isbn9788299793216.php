    <h3>Sammendrag</h3>
    <p>Festskriftet ble laget i forbinndelse med at Egil Nysæter fratrådde som fylkesarkivar i Hordaland. Festskriftet speiler Nysæters arbeid innenfor arkiv og lokalhistorie. Her er det artikler om utviklinga i lokalhistorisk forsking, om fotografi som kjelde, om etableringen av interkommunalt arkiv på Nord-Vestlandet, om arkiv og etikk, om formidling av arkivinformasjon, om bevaring av elektroniske arkiv og om statlig arkivpolitikk for å nemne noen av temaene.</p>
    <h3>Om redaktørene</h3>
    <h4>Anne Aune</h4>
    <h4>Valderhaug Gudmund</h4>
    <p>Gudmund Valderhaug har undervist i arkiv ved Institutt for arkiv, bibliotek og informasjonsfag ved Høgskolen i oslo og Akershus sidan 2004. Han er cand. Philol. Med historie hovudfag frå Universitetet i bergen.</p>
    <p>Han har arbeidd som arkivar ved Bergen Byarkiv (1983-86), interkommunalt arkiv i Hardoland (1987-2003) og Hordaland Fylkesarkiv (2006-10) og var leiar for Landslaget for lokal og privatarkiv frå 1997 til 2003. Frå 2003-2006 var han avdelingsdirektør i ABM-utvikling med ansvar for arkivfeltet. Han er redaktør av TidsskriftetArkiv og står bak bloggen <a href="http://depotdrengen.wordpress.com">depotdrengen.wordpress.com</a></p>
