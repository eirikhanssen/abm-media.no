    <h3>Sammendrag</h3>
    <p>Boka inneholder en samling av tekster som er blitt til gjennom 50 år i skole og utdanning. Samlingen starter med en prolog som heter «den gang og nå». Den kan leses som Per Østeruds dannelsesreise. Her er det vist til to bærebjelker i hans livsløp. Den ene er forholdet mellom folkelig og akademisk dannelse. Den andre er forholdet mellom det å utdanne seg, kvalifisere seg for et yrke, og det å utvikle seg som mennesker, som person og samfunnsborger med nærhet til omgivelsen. Et must for pedagogiske utdanninger!</p>
    <h3>Om forfatteren</h3>
    <h4>Per Østerud</h4>
    <p>Per Østerud er aktiv pensjonist ved seniorsenteret ved Høgskolen i Oslo og Akershus. Han har erfaring både som lærer, rektor og utreder. Han var blant annet rektor ved sagene Lærerskole og den sammenslåtte Oslo og Sagene Lærerskole.</p>
